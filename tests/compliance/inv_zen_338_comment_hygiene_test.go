func reportViolations(t *testing.T, invariant string, vios []string) {
	t.Helper()
	if len(vios) == 0 {
		return
	}
	t.Errorf("%s: %d violation(s) detected:", invariant, len(vios))
	for i, v := range vios {
		if i >= 20 {
			t.Logf("      ... and %d more", len(vios)-i)
			break
		}
		t.Logf("      %s", v)
	}
}

func formatViolation(path string, lineno int, line string) string {
	return filepath.Base(path) + ":" + strconv.Itoa(lineno) + ": " + strings.TrimSpace(line)
}

var (
	goPlanN   = regexp.MustCompile(`^\s*//\s*Plan [0-9]+`)
	goVersion = regexp.MustCompile(`^\s*//\s*v0\.[0-9]+\.[0-9]+`)

	goMarker = regexp.MustCompile(`^\s*//\s*(TODO|FIXME|XXX|HACK|NOTE)([^(\[]|\s)`)
	goAILeak = regexp.MustCompile(`(?i)^\s*//\s*(added by claude|generated by ai|created by claude|authored by ai)`)
)

var (
	pyPlanN   = regexp.MustCompile(`^\s*#\s*Plan [0-9]+`)
	pyVersion = regexp.MustCompile(`^\s*#\s*v0\.[0-9]+\.[0-9]+`)
	pyMarker  = regexp.MustCompile(`^\s*#\s*(TODO|FIXME|XXX|HACK|NOTE)([^(\[]|\s)`)
	pyAILeak  = regexp.MustCompile(`(?i)^\s*#\s*(added by claude|generated by ai|created by claude|authored by ai)`)
)

func TestInvZen338_NoPlanNRotInGo(t *testing.T) {
	var violations []string
	walkGoSource(t, func(path string, lineno int, line string) {
		if goPlanN.MatchString(line) {
			violations = append(violations, formatViolation(path, lineno, line))
		}
	})
	reportViolations(t, "inv-zen-338", violations)
	if len(violations) > 0 {
		t.Logf("auto-fix: bash scripts/comment_prepass.sh --apply")
	}
}

