func repoRoot338(t *testing.T) string {
	t.Helper()
	dir, err := os.Getwd()
	if err != nil {
		t.Fatalf("repoRoot: getwd: %v", err)
	}
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			t.Fatalf("repoRoot: go.mod not found walking up from %s", dir)
		}
		dir = parent
	}
}

func skipCommentHygieneDir(base string) bool {
	switch base {
	case ".git", "vendor", "testdata", "node_modules", ".venv", "venv", ".tox",
		".mypy_cache", ".pytest_cache", "__pycache__":
		return true
	default:
		return false
	}
}

func walkGoSource(t *testing.T, check func(path string, lineno int, line string)) {
	t.Helper()
	root := repoRoot338(t)
	for _, subdir := range []string{"internal", "cmd"} {
		err := filepath.Walk(filepath.Join(root, subdir), func(path string, info os.FileInfo, err error) error {
			if err != nil {
				return err
			}
			if info.IsDir() {
				base := filepath.Base(path)
				if skipCommentHygieneDir(base) {
					return filepath.SkipDir
				}
				return nil
			}
			if !strings.HasSuffix(path, ".go") {
				return nil
			}
			f, err := os.Open(path)
			if err != nil {
				return err
			}
			defer f.Close()
			scanner := bufio.NewScanner(f)
			scanner.Buffer(make([]byte, 1024*1024), 1024*1024)
			lineno := 0
			for scanner.Scan() {
				lineno++
				check(path, lineno, scanner.Text())
			}
			return scanner.Err()
		})
		if err != nil {
			t.Fatalf("walkGoSource: walk %s: %v", subdir, err)
		}
	}
}

