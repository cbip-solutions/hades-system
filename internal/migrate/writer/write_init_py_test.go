func TestRenderInitPy_Empty(t *testing.T) {
	t.Parallel()
	body := renderInitPy(nil)
	s := string(body)
	if !strings.Contains(s, "def register(ctx):") {
		t.Errorf("missing register def: %s", s)
	}
	if !strings.Contains(s, "pass") {
		t.Errorf("empty body must be `pass`: %s", s)
	}
}

func TestRenderInitPy_Deterministic(t *testing.T) {
	t.Parallel()
	calls := []string{
		`ctx.register_skill("b", "skills/b/SKILL.md", description="b")`,
		`ctx.register_skill("a", "skills/a/SKILL.md", description="a")`,
	}
	a := renderInitPy(calls)
	b := renderInitPy(calls)
	if string(a) != string(b) {
		t.Errorf("non-deterministic")
	}

	idxA := strings.Index(string(a), `register_skill("a"`)
	idxB := strings.Index(string(a), `register_skill("b"`)
	if idxA == -1 || idxB == -1 || idxA > idxB {
		t.Errorf("calls not sorted lex: %s", a)
	}
}

