// TestRenderInitPy_PreservesHadesNamespaceIdempotent — Plan 18b Phase B B-7.
// Input calls already correctly under `hades:` namespace MUST be emitted
// unchanged (rewriter is idempotent).
func TestRenderInitPy_PreservesHadesNamespaceIdempotent(t *testing.T) {
	t.Parallel()
	calls := []string{
		`ctx.register_command("hades:write-plan", handler=write_plan_handler, description="x")`,
	}
	body := string(renderInitPy(calls))
	if !strings.Contains(body, `ctx.register_command("hades:write-plan"`) {
		t.Errorf("expected 'hades:write-plan' to be PRESERVED in output (idempotent)\nbody:\n%s", body)
	}

	if strings.Contains(body, `"hades:hades:`) {
		t.Errorf("DOUBLE-PREFIXING bug: 'hades:hades:' appeared in output\nbody:\n%s", body)
	}
}

// TestRenderInitPy_NamespaceRewriteLeavesNonRegisterCommandUntouched —
// The rewriter MUST only touch register_command first positional args.
// register_skill calls (which take a name string in the first positional
// too) MUST NOT be rewritten — skill names live in a separate namespace
// per master §"Cross-phase type discipline".
func TestRenderInitPy_NamespaceRewriteLeavesNonRegisterCommandUntouched(t *testing.T) {
	t.Parallel()
	calls := []string{
		`ctx.register_skill("zen-swarm", "skills/zen-swarm/SKILL.md", description="x")`,
		`ctx.register_skill("brainstorm", "skills/brainstorm/SKILL.md", description="x")`,
	}
	body := string(renderInitPy(calls))
	if !strings.Contains(body, `ctx.register_skill("zen-swarm"`) {
		t.Errorf("register_skill 'zen-swarm' must be preserved (not rewritten)\nbody:\n%s", body)
	}
	if !strings.Contains(body, `ctx.register_skill("brainstorm"`) {
		t.Errorf("register_skill 'brainstorm' must be preserved (not rewritten)\nbody:\n%s", body)
	}
	if strings.Contains(body, `register_skill("hades:`) {
		t.Errorf("register_skill names must NOT be hades-prefixed (cross-phase type discipline)\nbody:\n%s", body)
	}
}
