func TestRenderInitPy_RegisterImportsHandlers(t *testing.T) {
	t.Parallel()
	calls := []string{
		`ctx.register_command("brainstorm", handler=brainstorm_handler, description="research-first brainstorm")`,
		`ctx.register_command("write-plan", handler=write_plan_handler, description="TDD plan writer")`,
		`ctx.register_skill("brainstorm", "skills/brainstorm/SKILL.md", description="brainstorm skill")`,
	}
	body := renderInitPy(calls)

	bodyStr := string(body)
	mustContain := []string{
		"from .commands.brainstorm import brainstorm_handler",
		"from .commands.write_plan import write_plan_handler",
		`ctx.register_command("hades:brainstorm", handler=brainstorm_handler`,
		`ctx.register_command("hades:write-plan", handler=write_plan_handler`,
		`ctx.register_skill("brainstorm",`,
	}
	for _, m := range mustContain {
		if !strings.Contains(bodyStr, m) {
			t.Errorf("rendered __init__.py missing %q\nbody:\n%s", m, bodyStr)
		}
	}

	pyImportRegisterAgainstStubCtx(t, body, []string{"brainstorm", "write_plan"})
}

