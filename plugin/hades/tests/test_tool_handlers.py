def test_post_tool_call_result_size_handles_unsized_objects():
    """Defensive guard: results without __len__ (int, custom objects, None)
    must not raise; result_size falls back to -1."""
    captured_payloads: list[dict] = []

    def fake_invoke(event_name, payload):
        captured_payloads.append(payload)
        return 0

    with patch(
        "hermes_plugins.hades.hooks.tool_handlers.invoke_event_poster",
        side_effect=fake_invoke,
    ):
        with patch.dict(os.environ, {}, clear=False):
            os.environ.pop("ZEN_HOOK_DRY_RUN", None)
                                                                        
