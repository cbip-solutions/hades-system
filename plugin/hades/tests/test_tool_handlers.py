def test_post_tool_call_emits_file_edited_for_write_class_tools():
    """post_tool_call with write_file tool_name should emit a derived
    `file.edited` event in addition to `post_tool_call`. Payload field-set
    is documented contract: tool_name, args_summary, task_id, session_id,
    tool_call_id, hook_event_name, result_kind, result_size."""
    captured: list[tuple[str, dict]] = []

    def fake_invoke(event_name, payload):
        captured.append((event_name, payload))
        return 0

    with patch(
        "hermes_plugins.hades.hooks.tool_handlers.invoke_event_poster",
        side_effect=fake_invoke,
    ):
        with patch.dict(os.environ, {}, clear=False):
            os.environ.pop("ZEN_HOOK_DRY_RUN", None)
            post_tool_call(
                tool_name="write_file",
                args={"path": "/tmp/foo.txt", "content": "hi"},
                task_id="task-1",
                session_id="sess-1",
                tool_call_id="tc-1",
                result="written",
            )
    event_names = [e for e, _ in captured]
    assert "post_tool_call" in event_names
    assert "file.edited" in event_names

                                                                     
                                                                          
    post_payload = next(p for e, p in captured if e == "post_tool_call")
    assert post_payload["tool_name"] == "write_file"
    assert post_payload["task_id"] == "task-1"
    assert post_payload["session_id"] == "sess-1"
    assert post_payload["tool_call_id"] == "tc-1"
    assert post_payload["hook_event_name"] == "post_tool_call"
    assert "args_summary" in post_payload
    assert post_payload["result_kind"] == "str"
    assert post_payload["result_size"] == len("written")


