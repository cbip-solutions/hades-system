def test_extract_commit_message_double_quoted():
    cmd = 'git commit -m "feat(foo): add bar"'
    assert extract_commit_message(cmd) == "feat(foo): add bar"


def test_extract_commit_message_single_quoted():
    cmd = "git commit -m 'fix(bar): correct baz'"
    assert extract_commit_message(cmd) == "fix(bar): correct baz"


def test_extract_commit_message_no_dash_m():
    cmd = "git commit"
    assert extract_commit_message(cmd) == ""


def test_extract_commit_message_with_escaped_quotes():
                                                            
    cmd = 'git commit -m "feat: \\"escaped\\" inside"'
    out = extract_commit_message(cmd)
                                                                            
    assert "feat:" in out


def test_extract_commit_message_non_string_input():
    """Defensive guard: non-string input (forward-compat for any future
    caller passing args directly) returns empty string without raising."""
    assert extract_commit_message(None) == ""
    assert extract_commit_message(12345) == ""
    assert extract_commit_message({"command": "git commit -m 'x'"}) == ""


                                                                               


